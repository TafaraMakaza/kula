class FoodItem {
  int id;
  String vendor;
  String name;
  String description;
  double price;
  String image;
  int quantity;

  FoodItem({
    @required this.id,
    @required this.vendor,
    @required this.name,
    @required this.description,
    @required this.price,
    @required this.image,
    this.quantity = 1,
  });

  void incrementQuantity() {
    this.quantity = this.quantity + 1;
  }

  void decrementQuantity() {
    this.quantity = this.quantity - 1;
  }

  String toString() {
    return "${this.quantity} X ${this.name} X ${this.price}";
  }
}
